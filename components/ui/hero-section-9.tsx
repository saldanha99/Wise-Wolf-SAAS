import { motion } from 'framer-motion';
import { Button, type ButtonProps } from '../../components/ui/button';
import { cn } from '../../lib/utils';
import React from 'react';

// Define the props for reusability
interface StatProps {
    value: string;
    label: string;
    icon: React.ReactNode;
}

interface ActionProps {
    text: string;
    onClick: () => void;
    variant?: ButtonProps['variant'];
    className?: string;
}

interface HeroSectionProps {
    title: React.ReactNode;
    subtitle: string;
    actions: ActionProps[];
    stats: StatProps[];
    images: string[];
    className?: string;
}

// Animation variants for Framer Motion
const containerVariants = {
    hidden: { opacity: 0 },
    visible: {
        opacity: 1,
        transition: {
            staggerChildren: 0.2,
        },
    },
};

const itemVariants = {
    hidden: { opacity: 0, y: 20 },
    visible: {
        opacity: 1,
        y: 0,
        transition: {
            duration: 0.5,
        },
    },
};

const imageVariants = {
    hidden: { opacity: 0, scale: 0.8 },
    visible: {
        opacity: 1,
        scale: 1,
        transition: {
            duration: 0.5,
            ease: 'easeOut',
        },
    },
};

const floatingVariants = {
    animate: {
        y: [0, -8, 0],
        transition: {
            duration: 3,
            repeat: Infinity,
            ease: 'easeInOut',
        },
    },
};

const HeroSection = ({ title, subtitle, actions, stats, images, className }: HeroSectionProps) => {
    return (
        <section className={cn('w-full overflow-hidden py-12 sm:py-24 bg-transparent', className)}>
            <div className="container mx-auto grid grid-cols-1 items-center gap-12 lg:grid-cols-2 lg:gap-8 px-6">
                {/* Left Column: Text Content */}
                <motion.div
                    className="flex flex-col items-center text-center lg:items-start lg:text-left z-20 relative"
                    variants={containerVariants}
                    initial="hidden"
                    animate="visible"
                >
                    <motion.h1
                        className="text-4xl font-bold tracking-tight text-white sm:text-6xl drop-shadow-md"
                        variants={itemVariants}
                    >
                        {title}
                    </motion.h1>
                    <motion.p className="mt-6 max-w-md text-lg text-blue-100 font-medium" variants={itemVariants}>
                        {subtitle}
                    </motion.p>
                    <motion.div className="mt-8 flex flex-wrap justify-center gap-4 lg:justify-start" variants={itemVariants}>
                        {actions.map((action, index) => (
                            <Button key={index} onClick={action.onClick} variant={action.variant} size="lg" className={action.className}>
                                {action.text}
                            </Button>
                        ))}
                    </motion.div>
                    <motion.div className="mt-12 flex flex-wrap justify-center gap-8 lg:justify-start" variants={itemVariants}>
                        {stats.map((stat, index) => (
                            <div key={index} className="flex items-center gap-3 backdrop-blur-sm bg-white/10 p-3 rounded-xl border border-white/20">
                                <div className="flex h-10 w-10 items-center justify-center rounded-full bg-white/20 text-white">{stat.icon}</div>
                                <div>
                                    <p className="text-xl font-bold text-white">{stat.value}</p>
                                    <p className="text-sm text-blue-200">{stat.label}</p>
                                </div>
                            </div>
                        ))}
                    </motion.div>
                </motion.div>

                {/* Right Column: Image Collage */}
                <motion.div
                    className="relative h-[400px] w-full sm:h-[500px]"
                    variants={containerVariants}
                    initial="hidden"
                    animate="visible"
                >
                    {/* Decorative Shapes */}
                    <motion.div
                        className="absolute -top-4 left-1/4 h-16 w-16 rounded-full bg-blue-200/50 dark:bg-blue-800/30 blur-xl"
                        variants={floatingVariants}
                        animate="animate"
                    />
                    <motion.div
                        className="absolute bottom-0 right-1/4 h-12 w-12 rounded-lg bg-purple-200/50 dark:bg-purple-800/30 blur-xl"
                        variants={floatingVariants}
                        animate="animate"
                        style={{ transitionDelay: '0.5s' }}
                    />

                    {/* Images */}
                    <motion.div
                        className="absolute left-1/2 top-0 h-48 w-48 -translate-x-1/2 rounded-2xl bg-white/10 p-2 shadow-2xl backdrop-blur-sm border border-white/20 sm:h-64 sm:w-64 rotate-[-6deg] hover:rotate-0 transition-transform duration-500 z-10"
                        style={{ transformOrigin: 'bottom center' }}
                        variants={imageVariants}
                    >
                        <img src={images[0]} alt="Student learning" className="h-full w-full rounded-xl object-cover" />
                    </motion.div>
                    <motion.div
                        className="absolute right-0 top-1/3 h-40 w-40 rounded-2xl bg-white/10 p-2 shadow-2xl backdrop-blur-sm border border-white/20 sm:h-56 sm:w-56 rotate-[12deg] hover:rotate-0 transition-transform duration-500 z-20"
                        style={{ transformOrigin: 'left center' }}
                        variants={imageVariants}
                    >
                        <img src={images[1]} alt="Tutor assisting" className="h-full w-full rounded-xl object-cover" />
                    </motion.div>
                    <motion.div
                        className="absolute bottom-10 left-0 h-32 w-32 rounded-2xl bg-white/10 p-2 shadow-2xl backdrop-blur-sm border border-white/20 sm:h-48 sm:w-48 rotate-[-15deg] hover:rotate-0 transition-transform duration-500 z-30"
                        style={{ transformOrigin: 'top right' }}
                        variants={imageVariants}
                    >
                        <img src={images[2]} alt="Collaborative discussion" className="h-full w-full rounded-xl object-cover" />
                    </motion.div>
                </motion.div>
            </div>
        </section>
    );
};

export default HeroSection;
